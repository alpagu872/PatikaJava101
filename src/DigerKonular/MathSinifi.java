package DigerKonular;

public class MathSinifi {
    public static void main(String[] args) {
        System.out.println(Math.abs(-5456)); // MUTLAK DEĞER ALMA
        System.out.println(Math.cbrt(8)); //KÜPKÖK ALMA
        System.out.println(Math.ceil(1.00005)); //EN YAKIN TAM SAYIYA YUVARLAMA
        System.out.println(Math.floor(1.1)); //EN YAKIN ALT DEĞER
        System.out.println(Math.log10(5)); // 10 TABANINDA LOGARİTMA
        System.out.println(Math.max(100, 200)); //İki sayı arasında max
        System.out.println(Math.pow(2, 5)); // ÜS ALMA İŞLEMİ 2 BASE 5 EXP = 32
        System.out.println(Math.random()); //0 ile 1 arasında rastgele değer üretir
        System.out.println(Math.random() * 100); //0-100
        System.out.println(Math.round(10.9));// En yakın int'e yuvarlama
        System.out.println(Math.signum(-5));//sayının negatif mi pozitif mi olduğunu geri döndürüyor
        System.out.println(Math.sqrt(4));//KAREKÖK ALMA
    }
}
